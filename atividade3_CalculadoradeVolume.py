# Definição das dimensões da caixa
comprimento = 12  # cm
largura = 14      # cm
altura = 20       # cm

# Cálculo do volume
volume = comprimento * largura * altura

# Exibição do resultado
print("3 - Calculadora de Volume")
print(f"Dimensões: {comprimento}cm x {largura}cm x {altura}cm")
print(f"Volume: {volume} cm³")
